char	*ft_strupcase(char *str)
{
	int	id;

	id = 0;
	while (str[id] != '\0')
	{
		if (str[id] >= 97 && str[id] <= 122)
			str[id] = str[id] - 32;
		id++;
	}
	return (str);
}
